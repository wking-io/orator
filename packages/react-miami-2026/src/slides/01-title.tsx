import { defaults, initDefaults, patchScene, type WarpParams } from "../shaders/warp-params";

interface UnicornScene {
  destroy(): void;
  resize(): void;
  paused: boolean;
}

export function TitleSlide(handle: Handle) {
  initDefaults(warpData as any);
  let scene: UnicornScene | null = null;
  let observer: ResizeObserver | null = null;
  let shaderEl: HTMLDivElement | null = null;
  let scriptEl: HTMLScriptElement | null = null;
  let debounceTimer: ReturnType<typeof setTimeout> | null = null;

  handle.signal.addEventListener("abort", () => {
    if (debounceTimer) clearTimeout(debounceTimer);
    observer?.disconnect();
    observer = null;
    scene?.destroy();
    scene = null;
    scriptEl?.remove();
    scriptEl = null;
  });

  function buildScene(params: WarpParams) {
    const patched = patchScene(warpData as any, params);
    return scaleScene(patched, 288);
  }

  function mountScene(el: HTMLDivElement, data: ReturnType<typeof buildScene>) {
    scene?.destroy();
    scriptEl?.remove();

    const scriptId = `us-warp-${handle.id}`;
    const script = document.createElement("script");
    script.id = scriptId;
    script.type = "application/json";
    script.textContent = JSON.stringify(data);
    document.body.appendChild(script);
    scriptEl = script;

    if (!el.id) el.id = `us-warp-el-${handle.id}`;

    UnicornStudioSDK.addScene({
      elementId: el.id,
      filePath: scriptId,
      scale: 1,
      dpi: 1,
      fps: 60,
      production: true,
    }).then((s: UnicornScene) => {
      scene = s;
      observer?.disconnect();
      observer = new ResizeObserver(() => scene?.resize());
      observer.observe(el);
    });
  }

  function handleParamsChange(params: WarpParams) {
    if (!shaderEl) return;
    if (debounceTimer) clearTimeout(debounceTimer);
    debounceTimer = setTimeout(() => {
      mountScene(shaderEl!, buildScene(params));
    }, 200);
  }

  return () => (
    <SlideRoot class="dark" notes="Welcome the audience and introduce yourself.">
      <div class="flex-1 flex flex-col justify-end mx-auto w-9/12 gap-[16%] mb-10">
        <div class="relative">
          <Text class="relative z-10 w-full h-auto" />
          <div
            class="absolute left-0 bottom-0 w-[35.5%] aspect-[506/320]"
            connect={(el: HTMLDivElement) => {
              shaderEl = el;
              mountScene(el, buildScene(defaults));
            }}
          />
        </div>
        <div class="flex items-center gap-[5%]">
          <GlobeGraphic class="w-[3.5%] h-auto -mt-[0.5%]" />
          <p class="font-heading">React Miami</p>
          <CheckeredGraphic class="h-[14%] w-auto" />
          <p class="font-heading">Will King</p>
        </div>
      </div>
      <DialPanel onParamsChange={handleParamsChange} originalData={warpData as any} />
    </SlideRoot>
  );
}

